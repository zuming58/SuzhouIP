export const DESTINATION_NAMES = SUZHOU_DESTINATIONS.map((item) => item.name);

export function findDestinations(text = "") {
  const value = String(text || "");
  return DESTINATION_NAMES
    .filter((name) => value.includes(name))
    .sort((a, b) => value.indexOf(a) - value.indexOf(b));
}

export function findDestinationMeta(name = "") {
  return SUZHOU_DESTINATIONS.find((item) => item.name === name || name.includes(item.name) || item.name.includes(name));
}

export function findPrefs(text = "") {
  const value = String(text || "");
  const prefs = [];
  if (/老人|父母|爸妈|少走|轻松|不累|腿脚/.test(value)) prefs.push("少走路");
  if (/孩子|小孩|亲子|研学|儿童/.test(value)) prefs.push("亲子");
  if (/园林|园子|古典/.test(value)) prefs.push("园林");
  if (/博物馆|展览|文物|文化|历史|研学/.test(value)) prefs.push("文博");
  if (/美食|小吃|吃|餐厅|午餐|晚饭|晚餐|苏帮菜|面|咖啡|茶/.test(value)) prefs.push("美食");
  if (/拍照|出片|打卡|夜景|日落/.test(value)) prefs.push("拍照");
  if (/商场|购物|买东西|逛街|奥莱/.test(value)) prefs.push("购物");
  if (/湿地|公园|自然|爬山|骑行|湖|太湖|金鸡湖|阳澄湖/.test(value)) prefs.push("自然");
  if (/古镇|水乡|老街/.test(value)) prefs.push("古镇水乡");
  return [...new Set(prefs)];
}

export function findPeople(text = "") {
  const value = String(text || "");
  if (/老人|父母|爸妈|长辈/.test(value)) return "老人";
  if (/孩子|小孩|亲子|一家|家庭/.test(value)) return "亲子家庭";
  if (/一个人|独自|自己/.test(value)) return "一人旅行";
  if (/情侣|对象|女朋友|男朋友|夫妻/.test(value)) return "情侣/夫妻";
  if (/朋友|同学|同事/.test(value)) return "朋友同行";
  if (/客户|商务|接待/.test(value)) return "商务接待";
  return "";
}

export function parseDays(text = "") {
  const value = String(text || "").replace(/\s+/g, "");
  const withoutOrdinals = value.replace(/第[一二三四五六七八九十两0-9]+[天日]/g, "");
  if (/(三|3)(天|日)(游|路线|行程)?/.test(withoutOrdinals)) return "three_day";
  if (/(两|二|2)(天|日)(游|路线|行程)?/.test(withoutOrdinals)) return "two_day";
  if (/(一|1)(天|日)(游|路线|行程)?/.test(withoutOrdinals)) return "one_day";
  if (/半天|半日/.test(withoutOrdinals)) return "half_day";
  return "";
}

export function parseStartTime(text = "") {
  const value = String(text || "");
  const numericMatch = value.match(/(上午|下午|晚上|早上)?\s*(\d{1,2})\s*[点:：]\s*(?:钟)?\s*(\d{0,2}|半)?/);
  if (numericMatch) {
    let hour = Number(numericMatch[2]);
    let minute = numericMatch[3] === "半" ? "30" : (numericMatch[3] || "00").padEnd(2, "0").slice(0, 2);
    if ((numericMatch[1] === "下午" || numericMatch[1] === "晚上") && hour < 12) hour += 12;
    return `${String(hour).padStart(2, "0")}:${minute}`;
  }

  const chineseMatch = value.match(/(上午|下午|晚上|早上)?\s*([零〇一二两三四五六七八九十]{1,3})点(?:钟)?(半|[零〇一二三四五六七八九十]{1,3}分?)?/);
  if (!chineseMatch) return "";
  let hour = chineseNumberToInt(chineseMatch[2]);
  if (!hour && hour !== 0) return "";
  let minute = "00";
  if (chineseMatch[3] === "半") {
    minute = "30";
  } else if (chineseMatch[3]) {
    const minuteText = chineseMatch[3].replace("分", "");
    minute = String(chineseNumberToInt(minuteText)).padStart(2, "0");
  }
  if ((chineseMatch[1] === "下午" || chineseMatch[1] === "晚上") && hour < 12) hour += 12;
  return `${String(hour).padStart(2, "0")}:${minute}`;
}

function chineseNumberToInt(text = "") {
  const digits = { 零: 0, "〇": 0, 一: 1, 二: 2, 两: 2, 三: 3, 四: 4, 五: 5, 六: 6, 七: 7, 八: 8, 九: 9 };
  if (!text) return 0;
  if (text === "十") return 10;
  if (text.includes("十")) {
    const [left, right] = text.split("十");
    const tens = left ? digits[left] : 1;
    const ones = right ? digits[right] : 0;
    return tens * 10 + ones;
  }
  if (text.length === 1) return digits[text];
  return Number([...text].map((char) => digits[char]).join(""));
}

export function isRouteRelated(text = "") {
  const value = String(text || "");
  return Boolean(
    findDestinations(value).length ||
      findPrefs(value).length ||
      findPeople(value) ||
      parseDays(value) ||
      parseStartTime(value) ||
      /想去|去哪|出去玩|来玩|旅游|路线|行程|安排|怎么玩|规划|到达|几点|点到|点钟到|逛|玩几天|几天/.test(value)
  );
}

